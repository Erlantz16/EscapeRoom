<script>
import Swal from 'sweetalert2';
import route from '../../vendor/tightenco/ziggy';
export default {
  name: 'juegos5',
  data() {
    return {
    }
  },

  
  mounted() {
    document.addEventListener('keydown', this.handleArrowKeys);

  },
  beforeDestroy() {
    document.removeEventListener('keydown', this.handleArrowKeys);
  },
  computed: {
    inputs() {
      return document.querySelectorAll('.table-container input[type="text"]');
    }
  },
  methods: {
    validarCampos() {
  console.log('validarCampos');
  const inputs = document.querySelectorAll('.table-container input[type="text"]');
  let inputsIncorrectos = 0;

  inputs.forEach(input => {
    const id = input.getAttribute('id');
    const valorInput = input.value.trim().toLowerCase();

    if (valorInput !== id) {
      input.classList.remove('correcto');
      input.classList.add('incorrecto');
      inputsIncorrectos++;
    } else {
      input.classList.remove('incorrecto');
      input.classList.add('correcto');
    }
  });

  if (inputsIncorrectos === 0) {
    Swal.fire({
      title: 'Zorionak!',
      text: 'Cruzigrama osatu dezu!',
      icon: 'success',
      confirmButtonText: 'Itzuli',
      heightAuto: false
    }).then((result) => {
      if (result.isConfirmed) {
        window.location.href = route('portada');

      } 
    });
  } 
}
,
    handleArrowKeys(event) {
      if (event.key.startsWith('Arrow')) {
        event.preventDefault(); // Evita el comportamiento predeterminado del navegador al presionar las teclas de flecha
        const currentInput = document.activeElement; // Obtiene el input actualmente enfocado
        const currentIndex = Array.from(this.inputs).indexOf(currentInput); // Obtiene el índice del input actual
        let nextIndex;

        if (event.key === 'ArrowUp' || event.key === 'ArrowLeft') {
          nextIndex = currentIndex > 0 ? currentIndex - 1 : this.inputs.length - 1; // Retrocede al input anterior
        } else if (event.key === 'ArrowDown' || event.key === 'ArrowRight') {
          nextIndex = currentIndex < this.inputs.length - 1 ? currentIndex + 1 : 0; // Avanza al siguiente input
        }

        const nextInput = this.inputs[nextIndex];
        nextInput.focus(); // Enfoca el próximo input
      }
    }
  }
}
</script>
